public class Operadores_Aritmeticos {
  public static void main(String[] args) {
    int uno = 5, dos = 7;
    int suma = uno + dos;
    int resta = uno - dos;
    int multiplicacion = uno * dos;
    float division = (float) uno / (float) dos;
    int resto = uno % dos;
    System.out.println("La suma es: " + suma + "\nLa resta es: " + resta + "\nLa multiplicacion es: " + multiplicacion + "\nLa division es: " + division + "\nEl resto es: " + resto);
  }
}
