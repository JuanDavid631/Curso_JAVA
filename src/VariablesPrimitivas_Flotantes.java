public class VariablesPrimitivas_Flotantes {
  public static void main(String[] args) {
    /* Float se usa para notacion cientifica y hacer conversiones con otras escalas de medidas */
    float variable = 2.5e2F;
      System.out.println(variable);
    /* Manejar double cuando se requiere decimales puntales */
    double variableDos = 2.67;
      System.out.println(variableDos);
  }
}
